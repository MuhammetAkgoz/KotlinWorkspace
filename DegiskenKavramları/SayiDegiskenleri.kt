package DegiskenKAVRAMLARI

fun main(args: Array<String>) {

    var sayi1: Byte = Byte.MIN_VALUE
    var sayi2: Byte = Byte.MAX_VALUE
    println("En küçük değer: "+ sayi1)
    println("En büyük değer: "+ sayi2)

    var sayi3: Short = Short.MAX_VALUE
    println("En büyük değer: "+sayi3)
    var sayi4: Short = Short.MIN_VALUE
    println("En küçük değeri: "+sayi4)

    var maxlong: Long= Long.MAX_VALUE
    println("En büyük değer: "+maxlong)
    var minlong: Long = Long.MIN_VALUE
    println("En küçük değeri: "+ minlong)

    var maxFloat: Float= Float.MAX_VALUE
    println("En büyük değer: "+maxFloat)
    var minFloat: Float= Float.MIN_VALUE
    println("En küçük değer: "+minFloat)

    var maxDouble: Double= Double.MAX_VALUE
    println("En büyük değer: "+maxDouble)
    var minDouble: Double = Double.MIN_VALUE
    println("En küçük değer: ${minDouble}")

    var intMAX= Int.MAX_VALUE
    println("En büyük değer $intMAX ")
    var intMin= Int.MIN_VALUE
    println("En küçük değer: "+intMin)


}

